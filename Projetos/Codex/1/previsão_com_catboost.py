def generate_prd_outputs(df_model, models):
    valid_features = [f for f in FEATURE_COLS if f in df_model.columns]
    latest_data = (
        df_model.dropna(subset=valid_features).groupby("Ticker").tail(1).copy()
    )
    results = []

    max_h = max(CONFIG["HORIZONS"])
    target_max = f"Target_D{max_h}"
    mae_global = (
        models[target_max].get_best_score()["validation"]["MAE"] * 100
    )

    for _, row in latest_data.iterrows():
        ticker = row["Ticker"]
        curr = row["Close"]

        ticker_res = {"Ticker": ticker, "Preço Atual": curr}

        for h in CONFIG["HORIZONS"]:
            target = f"Target_D{h}"
            pred_log = models[target].predict(row[valid_features])
            pred_log = np.clip(pred_log, -0.40, 0.40)
            ticker_res[f"D+{h}"] = np.exp(pred_log) * curr

        p_max = ticker_res[f"D+{max_h}"]
        var_max = ((p_max / curr) - 1) * 100

        ticker_res[f"Var % (D+{max_h})"] = var_max
        ticker_res["Direção"] = "ALTA" if var_max > 0 else "BAIXA"
        results.append(ticker_res)

    sort_col = f"Var % (D+{max_h})"
    report_df = pd.DataFrame(results).sort_values(by=sort_col, ascending=False)

    display(HTML(f"<h3>Tabela de Projeções (Top {CONFIG['TABLE_TOP_N']}) | Erro Médio Global (MAE D+{max_h}): {mae_global:.1f}%</h3>"))

    fmt = {
        "Preço Atual": "{:.2f}",
        sort_col: "{:+.1f}%",
    }
    for h in CONFIG["HORIZONS"]:
        fmt[f"Proj. D+{h}"] = "{:.2f}"

    table_df = report_df.head(CONFIG["TABLE_TOP_N"])
    styled_table = table_df.style.format(fmt).hide(axis="index")
    display(styled_table)

    n_charts = CONFIG["CHART_TOP_N"]
    if n_charts <= 0:
        print("\n[i] Geração de gráficos desativada (CHART_TOP_N = 0).")
        return

    tickers_to_plot = report_df.head(n_charts)["Ticker"].unique()
    n_rows = max(1, (len(tickers_to_plot) + 1) // 2)

    display(HTML(f"<h3>Trajetórias Preditivas (Top {len(tickers_to_plot)})</h3>"))
    fig, axes = plt.subplots(n_rows, 2, figsize=(15, 5 * n_rows))

    if hasattr(axes, "flatten"):
        axes = axes.flatten()
    else:
        axes = [axes]

    for i, ticker in enumerate(tickers_to_plot):
        ax = axes[i]
        hist = df_model[df_model["Ticker"] == ticker].tail(30)
        ax.plot(
            hist["Date"],
            hist["Close"],
            color="#2c3e50",
            lw=2,
            label="Histórico",
        )

        t_now = hist["Date"].iloc[-1]
        ax.axvline(x=t_now, color="red", ls="--", alpha=0.5)

        res_t = report_df[report_df["Ticker"] == ticker].iloc[0]
        future_dates = [
            t_now + pd.Timedelta(days=d) for d in CONFIG["HORIZONS"]
        ]
        projs = [res_t[f"Proj. D+{d}"] for d in CONFIG["HORIZONS"]]

        ax.plot(future_dates, projs, "o--", color="#27ae60", label="Predição")
        ax.fill_between(
            future_dates,
            [p * (1 - mae_global / 100) for p in projs],
            [p * (1 + mae_global / 100) for p in projs],
            color="#27ae60",
            alpha=0.1,
        )

        ax.set_title(f"Trajectory: {ticker}", fontweight="bold")
        ax.legend(fontsize=8)
        ax.grid(True, alpha=0.3)
        ax.tick_params(axis="x", rotation=30)

    for j in range(len(tickers_to_plot), len(axes)):
        fig.delaxes(axes[j])

    plt.tight_layout()
    plt.show()

