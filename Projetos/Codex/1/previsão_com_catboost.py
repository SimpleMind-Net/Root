def ingest_market_data(tickers: list, macro_tickers: list) -> pd.DataFrame:
    if os.path.exists(CONFIG["PARQUET_FILE"]):
        os.remove(CONFIG["PARQUET_FILE"])

    all_tickers = list(set(tickers + macro_tickers))
    raw_df = yf.download(
        all_tickers,
        period=CONFIG["PERIOD"],
        interval=CONFIG["INTERVAL"],
        auto_adjust=True,
        threads=True,
        progress=False,
    )

    macro_present = [c for c in macro_tickers if c in raw_df["Close"].columns]
    macro_df = raw_df["Close"][macro_present].copy()
    macro_df = macro_df.ffill().reset_index()

    main_present = [c for c in tickers if c in raw_df["Close"].columns]
    main_raw = raw_df.loc[:, (slice(None), main_present)]
    df = main_raw.stack(level=1, future_stack=True).reset_index()
    df.rename(columns={"level_1": "Ticker"}, inplace=True)
    df = df[
        ["Date", "Ticker", "Open", "High", "Low", "Close", "Volume"]
    ].dropna(subset=["Close"])

    counts = df["Ticker"].value_counts()
    valid_tickers = counts[counts >= CONFIG["MIN_HISTORY_DAYS"]].index
    df = df[df["Ticker"].isin(valid_tickers)]

    df.sort_values(by=["Ticker", "Date"], inplace=True)

    df = pd.merge(df, macro_df, on="Date", how="left")
    df[macro_present] = df.groupby("Ticker")[macro_present].ffill().bfill()

    df.to_parquet(CONFIG["PARQUET_FILE"])
    return df

