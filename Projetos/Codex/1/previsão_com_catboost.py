# SESSION 4: WALK-FORWARD & TUNING
# ==========================================
def walk_forward_split(df: pd.DataFrame, target_col: str):
    valid_features = [f for f in FEATURE_COLS if f in df.columns]
    valid_df = df.dropna(subset=[target_col] + valid_features).sort_values(
        by="Date"
    )

    split_idx = int(len(valid_df) * (1 - CONFIG["VAL_SIZE"]))

    X_train, y_train = (
        valid_df[valid_features].iloc[:split_idx],
        valid_df[target_col].iloc[:split_idx],
    )
    X_val, y_val = (
        valid_df[valid_features].iloc[split_idx:],
        valid_df[target_col].iloc[split_idx:],
    )

    return Pool(X_train, y_train, cat_features=["Ticker"]), Pool(
        X_val, y_val, cat_features=["Ticker"]
    )

class ObjectiveEvaluator:
    def __init__(self, train_pool, val_pool):
        self.tp, self.vp = train_pool, val_pool

    def __call__(self, trial):
        params = {
            "iterations": 500,
            "depth": trial.suggest_int("depth", 4, 8),
            "learning_rate": trial.suggest_float("learning_rate", 0.01, 0.1, log=True),
            "loss_function": "MAE",
            "eval_metric": "MAE",
            "thread_count": -1,
            "border_count": 128,
            "max_ctr_complexity": 1,
            "verbose": 0,
            "task_type": "CPU",
        }
        model = CatBoostRegressor(**params)
        model.fit(self.tp, eval_set=self.vp)
        return model.get_best_score()["validation"]["MAE"]

def tune_and_train(tp, vp):
    study = optuna.create_study(
        direction="minimize",
        pruner=optuna.pruners.MedianPruner(n_warmup_steps=10)
    )
    study.optimize(ObjectiveEvaluator(tp, vp), n_trials=CONFIG["N_TRIALS"])

    best_params = study.best_params
    best_params.update({
        "iterations": 1000,
        "loss_function": "MAE",
        "eval_metric": "MAE",
        "thread_count": -1,
        "border_count": 128,
        "max_ctr_complexity": 1,
        "verbose": 0,
        "task_type": "CPU",
    })

    model = CatBoostRegressor(**best_params)
    model.fit(tp, eval_set=vp)
    return model

