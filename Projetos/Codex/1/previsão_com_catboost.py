# SESSION 3: TARGET ENGINEERING
# ==========================================
def create_ticker_targets(group: pd.DataFrame) -> pd.DataFrame:
    res = group.copy()
    for h in CONFIG["HORIZONS"]:
        future_price = res["Mid_Price"].shift(-h)
        raw_log_ret = np.log(future_price / res["Mid_Price"])
        res[f"Target_D{h}"] = np.clip(raw_log_ret, -0.40, 0.40)
    return res

def build_targets(df: pd.DataFrame) -> pd.DataFrame:
    return (
        df.groupby("Ticker", group_keys=False)
        .apply(create_ticker_targets)
        .reset_index(drop=True)
    )

