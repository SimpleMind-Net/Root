@numba.jit(nopython=True, nogil=True)
def compute_mid_price(
    high: np.ndarray, low: np.ndarray, close: np.ndarray
) -> np.ndarray:
    return (high + low + 2.0 * close) / 4.0

def calculate_technical_indicators(group: pd.DataFrame) -> pd.DataFrame:
    res = group.copy()
    high = res["High"].values
    low = res["Low"].values
    close = res["Close"].values
    volume = res["Volume"].values
    mid_price = res["Mid_Price"].values

    res["Log_Ret"] = np.insert(
        np.log(mid_price[1:] / mid_price[:-1]), 0, np.nan
    )
    res["RSI"] = talib.RSI(close, timeperiod=CONFIG["RSI_PERIOD"])

    ma_vol = talib.SMA(volume, timeperiod=CONFIG["MA_VOL_PERIOD"])
    res["Rel_Volume"] = np.where(ma_vol == 0, 0, volume / ma_vol)

    res["OBV"] = talib.OBV(close, volume)
    res["ATR"] = talib.ATR(high, low, close, timeperiod=CONFIG["RSI_PERIOD"])

    for lag in CONFIG["LAG_DAYS"]:
        res[f"Lag_{lag}"] = res["Log_Ret"].shift(lag)
    return res

def apply_feature_engineering(df: pd.DataFrame) -> pd.DataFrame:
    df["Mid_Price"] = compute_mid_price(
        df["High"].values.astype(np.float32),
        df["Low"].values.astype(np.float32),
        df["Close"].values.astype(np.float32),
    )

    df = df.groupby("Ticker", group_keys=False).apply(
        calculate_technical_indicators
    )

    rolling_window = CONFIG["MIN_HISTORY_DAYS"]
    df["Log_Ret_Z"] = df.groupby("Ticker")["Log_Ret"].transform(
        lambda x: (x - x.rolling(rolling_window, min_periods=1).mean())
        / x.rolling(rolling_window, min_periods=1).std()
    )

    return df

